package org.pentaho.di.repository;

public class SecurityManagerCreationException extends Exception {
  private static final long serialVersionUID = 2278828147241579575L;

    public SecurityManagerCreationException() {
      super();
    }

    public SecurityManagerCreationException(String message, Throwable cause) {
      super(message, cause);
    }

    public SecurityManagerCreationException(String message) {
      super(message);
    }

    public SecurityManagerCreationException(Throwable cause) {
      super(cause);
    }
}
