package org.pentaho.di.ui.spoon.delegates;

import org.pentaho.di.core.logging.LogChannelInterface;
import org.pentaho.di.core.logging.LoggingObjectInterface;
import org.pentaho.di.core.logging.LoggingObjectType;
import org.pentaho.di.core.logging.SimpleLoggingObject;
import org.pentaho.di.ui.spoon.Spoon;

public abstract class SpoonDelegate
{
	public static final LoggingObjectInterface loggingObject = new SimpleLoggingObject("Spoon (delegate)", LoggingObjectType.SPOON, null);

	protected Spoon spoon;
	protected LogChannelInterface log;
	
	protected SpoonDelegate(Spoon spoon)
	{
		this.spoon = spoon;
		this.log = spoon.getLog();
	}
}
